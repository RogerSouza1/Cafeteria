    private double totalComanda(int comanda) {
        PagamentoDAO p1 = new PagamentoDAO();
        double total = 0;

        try {
            ResultSet todos = p1.buscarPedidos(comanda);

            while (todos.next()) {
                total += todos.getDouble("preco");
            }
            todos.close();
            return total;
        } catch (SQLException err) {
            JOptionPane.showMessageDialog(null, err.getMessage());
        }
        return total;
    }


