    private void btnfecharComandaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnfecharComandaMouseClicked
        PagamentoDAO p = new PagamentoDAO();

        int[] comandas = new int[21];
        for (int i = 0; i <= 20; i++) {
            comandas[i] = i;
        }
        
        int comanda = (comandas[this.comboComanda.getSelectedIndex()]);

        if (comanda != 0) {
            String totalText = this.txtTotal.getText();
            if (!totalText.isEmpty()) {
                totalText = totalText.replace("R$", "").replace(",", ".");
                double total = Double.parseDouble(totalText);

                if (total != 0) {
                    p.limparComanda(comanda);

                    this.comboComanda.setSelectedIndex(0);
                    this.txtTotal.setText("");

                    Pagamento pag = new Pagamento();

                    pag.setComanda(comanda);
                    pag.setTotal(total);
                    pag.setFormaPagamento("");
                    pag.setPedidoFechado("N");

                    p.incluir(pag);
                } else {
                    JOptionPane.showMessageDialog(null, "O valor total não pode ser zero.");
                }
            } else {
                JOptionPane.showMessageDialog(null, "O campo de total está vazio.");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Selecione uma comanda válida.");
        }

    }//GEN-LAST:event_btnfecharComandaMouseClicked

